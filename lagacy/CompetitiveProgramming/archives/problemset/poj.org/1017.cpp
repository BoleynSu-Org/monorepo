#include <iostream>
using namespace std;

int main()
{
    for (;;)
    {
        int n[6+2];
        for (int i=1;i<=6;i++) cin>>n[i];
        bool end=true;
        for (int i=1;i<=6;i++) if (n[i]) end=false;
        if (end) break;
        int answer=0;
        
        answer+=n[6];
        n[6]=0;
        
        answer+=n[5];
        if (n[5]*(6*6-5*5)>n[1]*(1*1)) n[1]=0;
        else n[1]-=n[5]*(6*6-5*5)/(1*1);
        n[5]=0;
        
        answer+=n[4];
        if (n[4]*(6*6-4*4)>n[2]*(2*2))
        {
           if (n[4]*(6*6-4*4)>n[2]*(2*2)+n[1]*(1*1)) n[1]=0;
           else n[1]-=(n[4]*(6*6-4*4)-n[2]*(2*2))/(1*1);
           n[2]=0;
        }
        else n[2]-=n[4]*(6*6-4*4)/(2*2);
        n[4]=0;
        
        answer+=n[3]/((6*6)/(3*3));
        n[3]=n[3]%((6*6)/(3*3));
        switch (n[3])
        {
               case 1:
                    answer+=1;
                    if (5>n[2])
                    {
                       if (((6*6-1*(3*3))-n[2]*(2*2))>n[1]*(1*1)) n[1]=0;
                       else n[1]-=((6*6-1*(3*3))-n[2]*(2*2))/(1*1);
                       n[2]=0;
                    }
                    else
                    {
                        if (((6*6-1*(3*3))-5*(2*2))>n[1]*(1*1)) n[1]=0;
                        else n[1]-=((6*6-1*(3*3))-5*(2*2))/(1*1);
                        n[2]-=5;
                    }
                    break;
               case 2:
                    answer+=1;
                    if (3>n[2])
                    {
                       if (((6*6-2*(3*3))-n[2]*(2*2))>n[1]*(1*1)) n[1]=0;
                       else n[1]-=((6*6-(6*6-2*(3*3)))-n[2]*(2*2))/(1*1);
                       n[2]=0;
                    }
                    else
                    {
                        if (((6*6-2*(3*3))-3*(2*2))>n[1]*(1*1)) n[1]=0;
                        else n[1]-=((6*6-2*(3*3))-3*(2*2))/(1*1);
                        n[2]-=3;
                    }
                    break;
               case 3:
                    answer+=1;
                    if (1>n[2])
                    {
                       if (((6*6-3*(3*3))-n[2]*(2*2))>n[1]*(1*1)) n[1]=0;
                       else n[1]-=((6*6-3*(3*3))-n[2]*(2*2))/(1*1);
                       n[2]=0;
                    }
                    else
                    {
                        if (((6*6-3*(3*3))-1*(2*2))>n[1]*(1*1)) n[1]=0;
                        else n[1]-=((6*6-3*(3*3))-1*(2*2))/(1*1);
                        n[2]-=1;
                    }
                    break;
        }
        
        answer+=(n[2]*(2*2)+n[1]*(1*1))/(6*6);
        if ((n[2]*(2*2)+n[1]*(1*1))%(6*6)!=0) answer+=1;
        n[2]=0;
        n[1]=0;
        cout<<answer<<endl;
    }
    return 0;
}
